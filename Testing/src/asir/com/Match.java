package asir.com;

public class Match {
 private int id;
 private String name;
 public Match(){
	 
 }
 
 
 
public Match(int id, String name) {
	super();
	this.id = id;
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
}
